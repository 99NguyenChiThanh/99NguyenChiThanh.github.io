    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (KTmaSV(txtMaSV.getText())) {
            int ret = JOptionPane.showConfirmDialog(null, "Mã[" + txtMaSV.getText() + "] Bạn có muốn xóa", "", JOptionPane.YES_NO_OPTION);
            if (ret == JOptionPane.NO_OPTION) {
                return;
            }
            try {
                String sqldeletSV = "delete  from sinhvien where masv='" + txtMaSV.getText() + "' ";
                String sqldeleteKQ = "delete from ketqua where masv='" + txtMaSV.getText() + "'";
                int y = statement.executeUpdate(sqldeleteKQ);
                int x = statement.executeUpdate(sqldeletSV);
                if (x >= 1 || y >= 1) {
                    JOptionPane.showMessageDialog(this, "Xoa sinh vien thanh cong.");
                    HienThi();
                    txtHoTen.setText("");
                    txtNgaySinh.setText("");
                    txtGioitinh.setText("");
                    txtQueQuan.setText("");
                    txtMaLop.setText("");
                    txtMaSV.setText("");
                    txtHoTen.requestFocus();
                }
            } catch (SQLException ex) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xoa sinh vien khong thanh cong.");
            txtHoTen.setText("");
            txtNgaySinh.setText("");
            txtGioitinh.setText("");
            txtQueQuan.setText("");
            txtMaLop.setText("");
            txtMaSV.setText("");
            txtHoTen.requestFocus();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

