    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtMaSV.getText())) {
            JOptionPane.showMessageDialog(this, "MãSV không được để trống.");
            txtMaSV.requestFocus();
            return;
        }
        else if (KTmaSV(txtMaSV.getText())) {
            int ret = JOptionPane.showConfirmDialog(null, "Mã[" + txtMaSV.getText() + "] đã tồn tại Bạn có muốn cập nhật ?", "", JOptionPane.YES_NO_OPTION);
            if (ret == JOptionPane.NO_OPTION) {
                return;
            }
            try {
                String sqlUpdate = "update sinhvien set hoten=N'" + txtHoTen.getText() + "', ngsinh='" + txtNgaySinh.getText() + "' , gioitinh=N'" + txtGioitinh.getText() + "' , quequan=N'" + txtQueQuan.getText() + "' , malop='" + txtMaLop.getText() + "' where masv='" + txtMaSV.getText() + "' ";
                int x = statement.executeUpdate(sqlUpdate);
                if (x >= 1) {
                    HienThi();
                }
            } catch (SQLException ex) {
            
            }
            } else {
                try {

                    String sqlInsert1 = "SET DATEFORMAT DMY INSERT INTO SINHVIEN  values('" + txtMaSV.getText() + "',N'" + txtHoTen.getText() + "','" + txtNgaySinh.getText() + "',N'" + txtGioitinh.getText() + "',N'" + txtQueQuan.getText() + "','" + txtMaLop.getText() + "')";
                    int y = statement.executeUpdate(sqlInsert1);

                    if (y >= 1) {
                        HienThi();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Them sinh vien khong thanh cong.");
                }
            }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        // TODO Auto-generated method stub
        if ("".equals(txtTK.getText())) {
            HienThi();
        } else {
            try {
                PreparedStatement prestament;
                String sql2 = "select * from sinhvien where masv=? or hoten=?  or gioitinh=? or quequan=? or malop=?";
                prestament = con.prepareStatement(sql2);
                prestament.setString(1, txtTK.getText());
                prestament.setString(2, txtTK.getText());
                prestament.setString(3, txtTK.getText());
                prestament.setString(4, txtTK.getText());
                prestament.setString(5, txtTK.getText());

                result = prestament.executeQuery();
                dtmInfoStudy.setRowCount(0);
                while (result.next()) {
                    String ma = result.getString("MASV");
                    String ten = result.getString("HOTEN");
                    String ns = result.getString("NGSINH");
                    String gt = result.getString("GIOITINH");
                    String qq = result.getString("QUEQUAN");
                    String ml = result.getString("MALOP");
                    Vector<Object> vec = new Vector<Object>();
                    vec.add(ma);
                    vec.add(ten);
                    vec.add(ns);
                    vec.add(gt);
                    vec.add(qq);
                    vec.add(ml);
                    dtmInfoStudy.addRow(vec);
                }
            } catch (SQLException ex) {
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

