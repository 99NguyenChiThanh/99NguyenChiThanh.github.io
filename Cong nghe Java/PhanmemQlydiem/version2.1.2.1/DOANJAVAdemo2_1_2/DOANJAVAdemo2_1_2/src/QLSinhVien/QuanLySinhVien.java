    private void KetNoiCSDL() {
        String strServer = "TOSHIBA-C855";
        String strDatebase = "QLSV";
        try {

            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String connectionURL = "jdbc:sqlserver://" + strServer + ":1433;databaseName=" + strDatebase
                    + ";integratedSecurity = true";
            con = DriverManager.getConnection(connectionURL);
            statement = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);

        } catch (SQLException ex1) {
            System.out.println("SQL Exception:" + ex1.toString());

        } catch (ClassNotFoundException ex2) {
            System.out.println(" Class Not Found Exception:" + ex2.toString());

        }

    }

    private void HienThi() {
        try {
            String sql = "select distinct masv,hoten,ngsinh,gioitinh,quequan,malop from SINHVIEN ";
            result = statement.executeQuery(sql);
            dtmInfoStudy.setRowCount(0);
            while (result.next()) {

                String ma = result.getString("MASV");
                String ten = result.getString("HOTEN");
                String ns = result.getString("NGSINH");
                String gt = result.getString("GIOITINH");
                String qq = result.getString("QUEQUAN");
                String ml = result.getString("MALOP");
//                int hk=result.getInt("HOCKY"); 
                Vector<Object> vec = new Vector<>();

                vec.add(ma);
                vec.add(ten);
                vec.add(ns);
                vec.add(gt);
                vec.add(qq);
                vec.add(ml);
//  

                dtmInfoStudy.addRow(vec);

            }

        } catch (SQLException ex) {

        }

    }

